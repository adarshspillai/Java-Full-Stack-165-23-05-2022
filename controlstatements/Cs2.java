package controlstatements;

public class Cs2 {

	public static void main(String[] args) {
		char vowels='e';
		switch(vowels) {
		case 'a':{
			System.out.println("a");
			break;
		}
		case 'e':{
			System.out.println("e");
			break;
		}
		case 'i':{
			System.out.println("i");
			break;
		}
		case 'o':{
			System.out.println("o");
			break;
		}
		case 'u':{
			System.out.println("u");
			break;
		}
		default:{
			System.out.println("invalid");
		}


		}

	}

}
